package Model;

import java.util.Date;

/**
 *
 * @author PCMSI
 */
public class Course {
     private int courseID, user_id, levelID;
    private String courseName, description, img, price, course_status; 
    private Date createTime; 

    public Course() {
    }

    public Course(int courseID, int user_id, int levelID, String courseName, String description, String img, String price, String course_status, Date createTime) {
        this.courseID = courseID;
        this.user_id = user_id;
        this.levelID = levelID;
        this.courseName = courseName;
        this.description = description;
        this.img = img;
        this.price = price;
        this.course_status = course_status;
        this.createTime = createTime;
    }

    public int getCourseID() {
        return courseID;
    }

    public void setCourseID(int courseID) {
        this.courseID = courseID;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getLevelID() {
        return levelID;
    }

    public void setLevelID(int levelID) {
        this.levelID = levelID;
    }

    public String getCourseName() {
        return courseName;
    }

    public void setCourseName(String courseName) {
        this.courseName = courseName;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getCourse_status() {
        return course_status;
    }

    public void setCourse_status(String course_status) {
        this.course_status = course_status;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }
    
    
}
